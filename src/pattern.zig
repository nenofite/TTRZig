pub const Pattern = [16]u8;

pub fn invert(comptime original: Pattern) Pattern {
    comptime {
        var ip: Pattern = original;
        for (ip[0..8]) |*b| {
            b.* = ~b.*;
        }
        return ip;
    }
}

pub fn transparent(comptime original: Pattern) Pattern {
    comptime {
        var ip = original;
        for (ip[0..8], ip[8..16]) |color, *alpha| {
            alpha.* = ~color;
        }
        return ip;
    }
}

